    private String[] getStreetNames() {

        try {

            List<String> list = locationDAO.getAllSreets();
            int size = list.size();
            String[] streetArray = new String[size];
            for (int i = 0; i < size; i++) {
                streetArray[i] = list.get(i);
            }
            return streetArray;

        } catch (Exception ex) {
            Logger.getLogger(PropertyView.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }


