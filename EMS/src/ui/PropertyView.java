    private void btnShowRecordsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowRecordsActionPerformed
        try {

            String street = autoComboBoxStreet.getSelectedItem().toString();
            String junction = autoComboBoxJunction.getSelectedItem().toString();
            location = locationDAO.getLocation(street, junction);

            Date fromDate = dateChooserFrom.getDate();
            Date toDate = dateChooserTo.getDate();

            if (fromDate == null || toDate == null) {
                JOptionPane.showMessageDialog(PropertyView.this, "Error: " + "Please select all two dates correctly", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (fromDate.after(toDate)) {
                JOptionPane.showMessageDialog(PropertyView.this, "Error: " + "Please select a valid date range", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            sensorTemperature = sensorDAO.getSensor(street, junction, "temperature");
            sensorRainfall = sensorDAO.getSensor(street, junction, "rainfall");
            sensorWind = sensorDAO.getSensor(street, junction, "wind");
            sensorAir = sensorDAO.getSensor(street, junction, "quality_of_air");

            List<Temperature> temperature = propertyRecordDAO.searchTemperature(fromDate, toDate);
            TemperatureTableModel modelTemperatureTable = new TemperatureTableModel(temperature);
            tblTemperature.setModel(modelTemperatureTable);

            List<Rainfall> rainfall = propertyRecordDAO.searchRainfall(fromDate, toDate);
            RainfallTableModel modelRainfallTable = new RainfallTableModel(rainfall);
            tblRainfall.setModel(modelRainfallTable);
            
            List<Wind> wind = propertyRecordDAO.searchWind(fromDate, toDate);
            WindTableModel modelWindTable = new WindTableModel(wind);
            tblWind.setModel(modelWindTable);
            
            List<QualityOfAir> air = propertyRecordDAO.searchQualityOfAir(fromDate, toDate);
            AirTableModel modelAirTable = new AirTableModel(air);
            tblAir.setModel(modelAirTable);
            

        } catch (Exception exc) {
            Logger.getLogger(EmployeeView.class.getName()).log(Level.SEVERE, null, exc);
            JOptionPane.showMessageDialog(PropertyView.this, "Error: " + exc, "Error", JOptionPane.ERROR_MESSAGE);
        }
        viewSummary();
    }//GEN-LAST:event_btnShowRecordsActionPerformed

    private void jTabbedPaneMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabbedPaneMouseClicked
        viewSummary();

    }//GEN-LAST:event_jTabbedPaneMouseClicked

    private void viewSummary() {
        int selectedTabIndex = jTabbedPane.getSelectedIndex();
        if (selectedTabIndex == 0 && sensorTemperature != null) {
            loadSummary(sensorTemperature);
        } else if (selectedTabIndex == 1 && sensorRainfall != null) {
            loadSummary(sensorRainfall);
        } else if (selectedTabIndex == 2 && sensorWind != null) {
            loadSummary(sensorWind);
        } else if (selectedTabIndex == 3 && sensorAir != null) {
            loadSummary(sensorAir);
        } else {
            lblSensorID.setText("");
            lblSerialNo.setText("");
            lblLongitude.setText("");
            lblLatitude.setText("");
        }
    }

    private void loadSummary(Sensor s) {
        lblSensorID.setText(s.getSensor_id());
        lblSerialNo.setText(s.getSerial_num());
        lblLongitude.setText(String.valueOf(location.getLongitude()));
        lblLatitude.setText(String.valueOf(location.getLattitude()));
    }

